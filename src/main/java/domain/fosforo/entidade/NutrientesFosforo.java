package domain.fosforo.entidade;

public class NutrientesFosforo {
    private double calcio;
    private double enxofre;
    private double magnesio;
    private double nitrogenio;

    public double getCalcio() {
        return calcio;
    }

    public void setCalcio(double calcio) {
        this.calcio = calcio;
    }

    public double getEnxofre() {
        return enxofre;
    }

    public void setEnxofre(double enxofre) {
        this.enxofre = enxofre;
    }

    public double getMagnesio() {
        return magnesio;
    }

    public void setMagnesio(double magnesio) {
        this.magnesio = magnesio;
    }

    public double getNitrogenio() {
        return nitrogenio;
    }

    public void setNitrogenio(double nitrogenio) {
        this.nitrogenio = nitrogenio;
    }
}
